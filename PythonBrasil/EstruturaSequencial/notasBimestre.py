print('Vamos calcular sua média escolar!')

bim1 = float(input('Insira a sua nota do primeiro bimestre: '))
bim2 = float(input('Insira a sua nota do segundo bimestre: '))
bim3 = float(input('Insira a sua nota do terceiro bimestre: '))
bim4 = float(input('Insira a sua nota do quarto bimestre: '))
media = ((bim1 + bim2 + bim3 + bim4) / 4)

print('A sua média é: ', media)
